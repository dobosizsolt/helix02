public class Math {
    public static int sum(int x, int y){
        return x + y;
    }
    public static int multiplication(int x, int y){
        return x * y;
    }
    public static int substraction(int x, int y){
        return x - y;
    }
    public static float dividing(int x, int y){
        return (float) x / y;
    }
}
